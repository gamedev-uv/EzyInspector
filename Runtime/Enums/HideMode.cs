namespace UV.EzyInspector
{
    /// <summary>
    /// The hide mode of the member in the inspector
    /// </summary>
    public enum HideMode
    {
        /// <summary>
        /// Hides the member from the inspector 
        /// </summary>
        Hide,

        /// <summary>
        /// Makes the member readonly 
        /// </summary>
        ReadOnly
    }
}
